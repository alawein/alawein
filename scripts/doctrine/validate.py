#!/usr/bin/env python3
"""Style validator for governed Alawein surfaces."""

from __future__ import annotations

import argparse
import re
import sys
from dataclasses import dataclass, field
from pathlib import Path
from typing import Literal

DEFAULT_ROOT = Path(__file__).resolve().parent.parent.parent

SKIP_DIRS = {
    ".git",
    "node_modules",
    "__pycache__",
    ".venv",
    "venv",
    "dist",
    "build",
    ".next",
    ".turbo",
    "coverage",
    "playwright-report",
    "test-results",
    "state",
}
# docs/archive/ and docs/internal/ are exempt from doctrine CI (CLAUDE.md rule
# 11). claude-agent-platform/ mirrors ~/.claude and carries vendored
# third-party skills; it is not an authored fleet-governed doc surface.
SKIP_PATH_PREFIXES = ("docs/archive/", "docs/internal/", "claude-agent-platform/")
SKIP_GENERATED_MARKERS = (
    "# AUTO-GENERATED",
    "# This file is generated",
    "<!-- AUTO-GENERATED -->",
    "<!-- generated by",
    "<!-- Generated by scripts/sync-readme.py.",
)

