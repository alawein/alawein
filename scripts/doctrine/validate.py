    r"\bpassionate about\b",
    r"\bleverages?\b",
    r"\bleveraging\b",
    r"\binnovative\b",
    r"\bcutting-edge\b",
    r"\bcomprehensive\b",
    r"\bexcited to\b",
    r"\bit'?s worth noting\b",
    r"\binterestingly\b",
    r"\bdelv(?:e|es|ed|ing)\b",
    r"\bdeep dive\b",
    r"\butilize\b",
    r"\bin today'?s world\b",
    r"\bmoving forward\b",
    r"\bmoreover\b",
    r"\bfurthermore\b",
    r"\bsynergy\b",
    r"\bholistic\b",
    r"\bgame-changing\b",
    r"\bgroundbreaking\b",
    r"\bseamless\b",
    r"\bseamlessly\b",
    r"\bstreamlin(?:e|es|ed|ing)\b",
    r"\brobust\b",
    r"\bpowerful\b",
    r"\bempower\b",
